					<p align="center"><font size="+1"><b>Conference Venue</b></font></p>
					
					<p>The conference will take place at the <b>Hungarian Institute in Paris, 92 rue Bonaparte</b>, in the 6th district, close to the Jardin du Luxembourg. Closest subway stations are Saint-Sulpice (line 4) and Mabillon (line 10). There are bus stops nearby (lines 58, 84 and 89).</p>
					
					<p>You can find a map on the <a href="http://www.magyarintezet.hu/citydir/city_10/terkep/eng/terkep.htm">website of the institute</a>. You may also check the <a href="http://www1.mappy.com/sidGVWkqFV1EPDMn21w/CFGMA?csl=m1&fsl=m1&gsl=m1&msl=m1&xsl=1&out=2&wnm1=92+rue+Bonaparte&wcm1=&nom1=&tnm1=Paris&tcm1=&a10m1=&pcm1=75006&ccm1=250&x=19&y=9">map on Mappy website</a>.</p>

					<p>You may also install <a href="http://www.peterbelanger.com/newton.html">Maps of Paris</a> for Mapper, Adam Tow's software to view maps on the Newton (these are maps of the Mass Transportation Network).</p>

					<p><b>Mass Transportation in Paris</b></p>
					
					<p>To travel within Paris, you can take busses, M&eacute;tro (subway), RER (train lines like the German S-Bahn) and regular train (operated by the SNCF). Busses, subway and RER are operated by the RATP and they have a good website with a <a href="http://www.citefutee.com/orienter/itineraire.php">route finder</a> (make sure you choose the French version, the English version seems to be inferior). You probably won't need to take regular trains as they only service the suburbs. Subways mostly service inner Paris (the end of the lines actually often go a little bit outside, in suburbs), as you can see on <a href="http://www.citefutee.com/picts/plans/pdf/reseaux/metro.pdf">this map</a>. RATP busses service the suburb (there are other companies as well).</p>

					<p>With a ticket (1.40 euros, but Parisians buy them by stacks of 10 for 10.50 euros), you can do any journey with <b>one</b> bus (including those not operated by the RATP) or any journey within the subway/RER network, except that with the RER, you cannot go to the suburb (you can with the subway).</p>

					<p>There are currently a lot of works within the subway and RER networks, most of them will be finished by the end of August. Be careful if you arrive before September. Posters about these usually are in three languages.</p>

					<p><b>Airports</b></p>

					<p>Paris has two main airports. The bigger, international one is Roissy-Charles de Gaulle (CDG) and we suggest that to go inside Paris you take the RER to inner Paris. Every RER train stop inside Paris. The ticket should be around 8.50 euros and the trip should last approximatively 45 minutes (actually, every other train is direct to Paris). Please note that you can travel anywhere inside Paris with this ticket within the subway/RER network. For example, if you were to come directly to the conference, you could take the RER from one of the two stations at the CDG airport to Saint-Michel Notre-Dame and take the subway line 4 from there to Saint Sulpice. Do not damage your ticket, you need it whenever you leave the RER network.</p>

					<p>As far as the Orly airport is concerned (ORY), we suggest that you take a bus to Denfert-Rochereau (called Orly Bus), a square in the south bank of Paris. You can then take another ticket to travel to your hotel. Denfert Rochereau is an important RER and subway station with several lines (4 and 6).</p>

					<p><b>Apple Expo</b></p>
					
					<p>In case you want to go to Apple Expo, it is located at Porte de Versailles. You can reach this place by subway (line 12) or by bus (PC).

