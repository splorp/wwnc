				<h1>Venue</h1>
				<img src="/image/warwickregis.jpg" alt="The Warwick Regis Hotel - San Francisco" width="254" height="350" border="0" align="right" style="margin:0 0 50px 15px;" />
				<p>The Worldwide Newton Conference 2006 will be held at the Warwick Regis Hotel in downtown San Francisco.</p>
				<p>Located on the corner of Geary and Taylor streets in the heart of the fashionable theatre district, the Warwick Regis Hotel is just two blocks west of Union Square and the world-famous cable car line. The hotel sits within close proximity to some of the most popular San Francisco travel destinations.</p>
				<p>The Warwick Regis Hotel is located 13 miles north of San Francisco International Airport and is easily accessable using the <a href="http://www.bart.gov/">Bay Area Rapid Transit</a> (BART) system.</p>
				<p>There are many options for reasonably priced meals near the hotel.</p>
				<p><b>Conference Rates</b></p>
				<p>The Warwick Regis Hotel has kindly offered a discounted room rate for conference attendees. The rate is $149 US per night single occupancy (based on availability. Please mention <b>WWNC</b> or <b>Worldwide Newton Conference</b> when registering with the hotel to qualify for the discounted rate.</p>
				<p>More information regarding the location, reservations, and facilities can be found on the hotel website:</p>
