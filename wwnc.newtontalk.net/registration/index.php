				<h1>How To Register</h1>
				<p>To register for the Worldwide Newton Conference 2006, please follow these simple steps:</p>
				<ol>
					<li>Complete the <a href="/registration/form/">registration form</a>.
					<p>The information gathered through the registration form will allow us to plan certain aspects of the event. Even if you are not sure whether you will be able to attend the conference, please register and indicate your interest anyway.</p></li>
					<li>Pay the <a href="/registration/payment/">registration fee</a>.
					<p>The conference registration fee for the 2006 conference is $40USD (approximately $45CAD / &#8364;35). Registration fees will be used to help cover the expenses associated with the conference. These expenses include, but are not limited to, rental of the conference space and network access. The Worldwide Newton Association is an international, non-profit organization.</p>
					<p>Please remember to pay for your registration. Thank you.</li>
					<li>See who is <a href="/registration/list/">already registered</a>.
					<p>You can also update your registration information from the attendee list.</p></li>
					<li>Watch for <a href="/">conference updates</a> on the home page.</p></li>
				</ol>
